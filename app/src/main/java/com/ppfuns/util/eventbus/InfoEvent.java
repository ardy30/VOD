public class InfoEvent {

    public static final int UPLOAD_FINISH = 1;
    public static final int QUITLOGIN = 2;
    public static final int CROP_MESSAGE = 3;
    public int id;
    public Object obj;

    public InfoEvent(){}

    public InfoEvent(int id, Object obj) {
        this.id = id;
        this.obj = obj;
    }


    @Override
    public String toString() {
        return "InfoEvent{" +
                "id=" + id +
                ", obj=" + obj +
                '}';
    }
}
